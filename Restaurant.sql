-- Create table --

CREATE TABLE "orders" (
    "row_id" int   NOT NULL PRIMARY KEY,
    "order_id" varchar(10)   NOT NULL,
    "created_at" datetime   NOT NULL,
    "item_id" varchar(10)   NOT NULL,
    "quantity" int   NOT NULL,
    "cust_id" int   NOT NULL,
    "delivery" boolean   NOT NULL,
    "add_id" int   NOT NULL
);

CREATE TABLE "customers" (
    "cust_id" int   NOT NULL PRIMARY KEY,
    "cust_firstname" varchar(50)   NOT NULL,
    "cust_lastname" varchar(50)   NOT NULL
);

CREATE TABLE "address" (
    "add_id" int   NOT NULL PRIMARY KEY,
    "delivery_address1" varchar(200)   NOT NULL,
    "delivery_address2" varchar(200)   NULL,
    "delivery_city" varchar(50)   NOT NULL,
    "delivery_zipcode" varchar(20)   NOT NULL
);

CREATE TABLE "item" (
    "item_id" varchar(10)   NOT NULL PRIMARY KEY,
    "sku" varchar(20)   NOT NULL,
    "item_name" varchar(50)   NOT NULL,
    "item_cat" varchar(50)   NOT NULL,
    "item_size" varchar(20)   NOT NULL,
    "item_price" decimal(10,2)   NOT NULL
);

CREATE TABLE "ingredient" (
    "ing_id" varchar(10)   NOT NULL PRIMARY KEY,
    "ing_name" varchar(200)   NOT NULL,
    "ing_weight" FLOAT   NOT NULL,
    "ing_meas" varchar(20)   NOT NULL,
    "ing_price" FLOAT   NOT NULL
);

CREATE TABLE "recipe" (
    "row_id" int   NOT NULL PRIMARY KEY,
    "recipe_id" varchar(20)   NOT NULL,
    "ing_id" varchar(10)   NOT NULL,
    "quantity" int   NOT NULL
);

CREATE TABLE "inventory" (
    "ivt_id" int   NOT NULL PRIMARY KEY,
    "item_id" varchar(10)   NOT NULL,
    "quantity" int   NOT NULL
);

CREATE TABLE "staff" (
    "staff_id" varchar(20)   NOT NULL PRIMARY KEY,
    "first_name" varchar(50)   NOT NULL,
    "last_name" varchar(50)   NOT NULL,
    "position" varchar(100)   NOT NULL,
    "hourly_rate" decimal(5,2)   NOT NULL
);

CREATE TABLE "shift" (
    "shift_id" varchar(20)   NOT NULL PRIMARY KEY,
    "day_of_week" varchar(10)   NOT NULL,
    "start_time" time   NOT NULL,
    "end_time" time   NOT NULL
);

CREATE TABLE "rota" (
    "row_id" int   NOT NULL PRIMARY KEY,
    "rota_id" varchar(20)   NOT NULL,
    "date" datetime   NOT NULL,
    "shift_id" varchar(20)   NOT NULL,
    "staff_id" varchar(20)   NOT NULL
);



